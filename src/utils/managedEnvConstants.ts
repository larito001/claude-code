/** 判断是否满足 is Provider Managed Env Var 对应的数据或状态。 */
export function isProviderManagedEnvVar(key: string): boolean {
  const upper = key.toUpperCase()
  return PROVIDER_MANAGED_ENV_VARS.has(upper)
}

